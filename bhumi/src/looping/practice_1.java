package looping;

public class practice_1 {
	
	public static void main(String[] args) {
		
			int num = 5;
			
			while (num<=50)
			{
					if(num%4==0)
					{
						
							System.out.println(num+" = Even number");
					}else {
							System.out.println(num+" = Odd number");
					}
					
					num++;
					
			}
			
	
	
				
		}
		
			
				
}
				


